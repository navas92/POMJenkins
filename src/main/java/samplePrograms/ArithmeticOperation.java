package samplePrograms;

public class ArithmeticOperation {
	
	int NumberOne = 12;
	int NumberTwo = 13;
	int Answer;
	
	public void Addition()
	
	{
		Answer = NumberOne + NumberTwo ;
		System.out.println("Sum of Numbers is:" + Answer );
	} 
	
	public int Subraction()
	
	{
		Answer = NumberOne - NumberTwo ;
		System.out.println("Subraction of Numbers is:" + Answer );
		return Answer;
	} 
	
	public int Multipulation()
	
	{
		Answer = NumberOne * NumberTwo ;
		System.out.println("Multipulation of Numbers is:" + Answer );
		return Answer;
	} 
	
	public void MultipulNumberonebynumbertwo()
	
	{
		Answer = NumberOne * NumberTwo ;
		System.out.println("Numbers is:" + Answer );
	} 
	
	public int dividenumbertwoby(int Divident)
	
	{
		Answer = NumberOne / Divident ;
		System.out.println("Numbers is:" + Answer );
		return Divident;
	} 
	
	
	
}
